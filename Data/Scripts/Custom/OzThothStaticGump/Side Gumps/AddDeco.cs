                return;

            int page = m_Page;

            if (button >= 1000000)
            {
                button -= 1000000;

                CommandSystem.Handle(
                    from,
                    String.Format(
                        "{0}M Add Static {1}",
                        CommandSystem.Prefix,
                        m_Types[button].m_BaseID
                    )
                );
            }
            else if (button >= 10000)
            {
                button -= 10000;

                page = button;
            }

            from.SendGump(new AddDecoStaticGump(page));
        }

        public class DecoSTInfo
        {
            public int m_BaseID;

            public DecoSTInfo(int baseID)
            {
                m_BaseID = baseID;
            }
        }
